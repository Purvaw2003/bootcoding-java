package com.bootcoding.java.basic;

public class Nestedloop1 {
    public static void main(String[] args) {
        for (int i = 1; i <= 4; i++) {
            for (int k = 1; k <= 4; i++) {
                System.out.print(k);


            }
            System.out.println(" ");


        }


    }
}